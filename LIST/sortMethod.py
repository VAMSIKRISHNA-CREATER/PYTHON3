# sorting a list using sort() function
 
my_list = [5, 2, 90, 24, 10]
 
 
# sorting in ascending order permanently
# changes the order of the list
my_list.sort()
print(my_list)
 
# sorting in descending order permanently
# changes the order of the list
my_list.sort(reverse=True)
print(my_list)
